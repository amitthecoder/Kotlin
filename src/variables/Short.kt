package variables

fun main(){

//    Short is an Integer data type that store numerical value from -32768 to 32767
//    Size = 16 Bits

    val x : Short = -32768 // Minimum value of Short Data type -32768
    println(x)

    val y : Short = 68 // Mid-Value value of Short Data type -32768
    println(y)

    val z : Short = 32767 // Maximum value of Short Data type 32767
    println(z)
}
package variables

fun main(){

//    Byte is an Integer data type that store numerical value from -128 to 127
//    Size = 8 Bits

    val x : Byte = -128 // Minimum value of Byte
    println(x)

    val y : Byte = 42 // Mid-value of -128 to 127
    println(y)

    val z : Byte = 127 // Maximum value of Byte
    println(z)

}
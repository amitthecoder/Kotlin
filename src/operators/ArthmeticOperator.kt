package operators

fun main(){

    val x : Int = 10
    val y : Int = 5

    // +, -, *, /, % - mathematical operators

    var z : Int = x + y
    println(z)      // println(x+y)

    z = x - y
    println(z)      // println(x-y)

    z = x * y
    println(z)      // println(x*y)

    z = x / y
    println(z)      // println(x/y)

    z = x % y
    println(z)      // println(x%y)

}